#include <iostream>
#include <cmath>

int main(void)
{
    setlocale(LC_ALL, ".1252");

    float base, expoente, indice, raiz;

    std::cout << "Informe a base: "; std::cin >> base;
    std::cin.ignore(80, '\n');
    std::cout << std::endl;

    std::cout << "Informe o expoente: "; std::cin >> expoente;
    std::cin.ignore(80, '\n');
    std::cout << std::endl;

    std::cout << "Informe o índice: "; std::cin >> indice;
    std::cin.ignore(80, '\n');
    std::cout << std::endl;

    raiz = pow(base, (expoente / indice));

    std::cout << "Raiz obtida = " << raiz << std::endl;
    std::cout << std::endl;

    std::cout << "Tecle <Enter> para encerrar... ";
    std::cin.get();
    return 0;
}
